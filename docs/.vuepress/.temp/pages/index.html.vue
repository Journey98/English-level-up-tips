<p>不久前（2017年7月初），备考托福的女神问了我一个问题：如何高效学习英语？</p>
<p>在我思考如何回答这个问题时，想到了在大四上一学期我考过 26 门课的经验（其中重修 19 门，当前学期 7 门），觉得我应该能勉强提供一些高效学习的小技巧。</p>
<p>与她交流了一番学习心得后，我惊讶于她在学习方面的热情竟是如此之高，同时也发现了她的学习方法存在一些不可取之处。</p>
<p>于是我写了一篇简单的文章零散地介绍了下我学习英语的小技巧，几天后她告诉我，希望我可以这些学习经验稍加整理，分享给更多有需要的人。</p>
<p>在此之前，我并不知道原来有那么多的同学在学习英语的道路上是一路走到黑的。
