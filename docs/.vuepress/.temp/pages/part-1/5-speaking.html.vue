<h2 id="大声说英语" tabindex="-1"><a class="header-anchor" href="#大声说英语" aria-hidden="true">#</a> 大声说英语</h2>
<p>大声说英语和小声说英语的效果是大大不同的。
以《新概念英语》第三册和第四册举例，在开始大声练习的之前，请先用心听，然后去朗读，最开始的时候不需要考虑连读、断句，争取把每个单词都清晰、标准的发音就行了。
再读的比较顺畅之后，再尝试模仿。</p>
<h2 id="交流" tabindex="-1"><a class="header-anchor" href="#交流" aria-hidden="true">#</a> 交流</h2>
<p>在刚开始使用纯英语与他人交流时，难免遇到&quot;心口不一&quot;的尴尬，内心明明想说的是&quot;真没有想到原来你的观点和我出奇的一致&quot;,嘴里却只能说出一个&quot;yeah&quot;；
明明内心万马奔腾、思如泉涌，口中还是只能蹦出&quot;yeah，yeah，yeah&quot;；
这种情况是相当普遍的。</p>
<p>很多人在开口说英语的时候，是先想好中文意思，然后经过自己的翻译，在脑海中去寻找相应的单词，一旦没有找到那个单词，就会卡住。
在经过大量的听力练习后，人脑会产生自然的语言反射，在面对一些特殊的情景会脱口而出，根本不需要经过大脑的翻译这层，比如&quot;fu*k、sh1t&quot;这类最简单的例子。</p>
